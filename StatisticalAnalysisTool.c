// Create a function to make linked lists and return a pointer to the linked list.
// The function has 2 parameters
//	- A string named "csvString".
//	- An integer variable named "size".
struct node* createLinkedList (char csvString[], int size) {
		// Dynamically allocate memory for a new array of double values named "values".
		double *values = malloc(size*sizeof(double));
		// If the array named "values" is NULL...
		while (values == NULL) {
			// Retry allocating memory for the array named "values".
			values = (double*)malloc(size*sizeof(double));
		}
		// Use the function named "readCSVRow" to read data from the CSV file.
		readCSVRow(values,csvString,size);
		// Dynamically allocate memory for a new node and create a pointer to that node named "Start".
		struct node* Start = malloc(size*sizeof(struct node));
		// Create a pointer named "current" and make it point to the node pointed to by the pointer named "Start".
		struct node* current = Start;
		// Loop through the following lines of a code a number of times equal to the value of the variable named "size".
		for (int i = 0; i < size; i++) {
		// Dynamically allocate memory for a new node and create a pointer to that node named "next".
		struct node *next = malloc(size*sizeof(struct node));
		// While the pointer named "next" is NULL.
		while (next == NULL) {
			// Retry dynamically allocating memory for the pointer named "next".
			next = (struct node*)malloc(size*sizeof(struct node));
		}
		// If this is not the last iteration of the loop...
		if (i != size-1) {
			// Store the value in the array named values at the index i in the current node of the linked list.
			current->Value = values[i];
			// Add the pointer named "next" to the linked list.
			current->nextNode = next;
			// Make the pointer named "Current" point to the node pointed to by the pointer named "next".
			current = next;
		} else { // Otherwise...
			// Store the value in the array named values at the index i in the current node of the linked list.
			current->Value = values[i];
			// Make the next in the linked list NULL.
			current->nextNode = NULL;
		}
	}
	// Free the memory allocated for the variable named "values".
	free(values);
	// Return the pointer named "Start".
	return Start;
}
// Function to delete a node in a linked list.
// This function has three inputs:
//	- A double pointer named "psearchIn" that points to another pointer which in turn points to a node.
//	- A double variable named "search".
//	- An integer variable named size.
void deleteNode (struct node** psearchIn, double search, int size) {
	// Deference the double pointer named "psearchIn" and store the resulting pointer in a variable named "searchIn".
	struct node* searchIn = *psearchIn;
	// Create a copy of the pointer named "searchIn" and store it in a variable named "previous".
	struct node* previous = searchIn;
	// Create an integer variable named "Count" with an initial value of 0.
	// This variable will be used to keep track of how many times the loop below iterates.
	int Count = 0;
	// Loop through the nodes of the linked list until the current node points to a memory location containing a value equal to the value of the variable named "double".
	// Store the previous node in the pointer named "previous" during each iteration of the loop.
	for (Count; Count < size; Count++) {
		// If the value stored in the variable named "Value" is pointed to by the pointer named "searchIn" inside the current element of the linked list...
		if (searchIn->Value == search) {
			// Break out of the loop.
			break;
		// Otherwise...
		} else {
			// Move on to the next node in the linked-list.
			previous = searchIn;
			searchIn = searchIn->nextNode;
		}
	}
	// If the value of the "Count" is less than the value of the variable named "size" but greater than zero...
	if (Count < size && Count > 0) {
		// Make the node after the node pointed to by the pointer named "searchIn" become the node after the node pointed to by the variable named "previous".
		previous->nextNode = searchIn->nextNode;
		// Free the memory that was allocated for the node pointed to by the variable named "searchIn".
		free(searchIn);
	} else if (Count == 0) { // Otherwise, if the value of the variable named "Count" is zero...
		// Make the pointer named "previous" point to the node that comes after the node pointed to by "searchIn".
		previous = searchIn->nextNode;
		// Make the double pointer named "psearchIn" point to the pointer named "previous".
		**psearchIn = *previous;
		// Free the memory that was allocated for the variable named "searchIn".
		free(searchIn);
	} else { // Otherwise...
		// Free the memory that was allocated for the variable named "searchIn".
		free(searchIn);
	}
}
