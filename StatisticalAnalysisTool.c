//	- A double pointer named "psearchIn" that points to another pointer which in turn points to a node.
//	- A double variable named "search".
//  - A double variable named "insert" containing a value will be inserted into the linked list.
//	- An integer variable named size.
void addNode (struct node** psearchIn, double search, double insert) {
	// Create an integer variable named "count" with an initial value of zero.
	// This will be used to keep track of how many times a loop iterates after the pointer below is initialized.
	int count = 0;
	// Dereference the double pointer named "psearchIn" and store the result in a pointer named "searchIn".
	struct node* searchIn = *psearchIn;
	// Loop through all the nodes in the linked list until the node that points to a memory address containing a value equal to the value of the variable named "search" is found.
	for (count; searchIn != NULL; count++) {
		// If the current node contains a pointer that points to a memory address containing a value equal to the value of the variable named "search"...
		if (searchIn->Value == search) {
			// Break out of the loop.
			break;
		// Otherwise...
		} else {
			// Move to the next node.
			searchIn = searchIn->nextNode;
		}
	}
	// Dynamically allocate memory for a new node and create a pointer to the node named "newNode".
	struct node *newNode = malloc(sizeof(struct node));
	// Make sure the dynamic memory allocation for the node named "newNode" worked.
	while (newNode == NULL) {
		newNode = (struct node*)malloc(sizeof(struct node));
	}
	// Create a pointer named "oldNode" that points to the node directly after the node pointed to by the pointer named "searchIn" in the linked list.
	struct node *oldNode;
	if (searchIn != NULL) {
		oldNode = searchIn->nextNode;
	}
	// Make the node named "newNode" become the node directly after the node pointed to by the pointer named "searchIn" in the linked list.
	searchIn->nextNode = newNode;
	// Make the value in the node pointed to by the pointer named "newNode" equal to the value stored in the variable named "insert".
	newNode->Value = insert;
	// Make the named pointed to by the pointer named "oldNode" become the node directly after the node pointed to by the pointer named "newNode".
	newNode->nextNode = oldNode;
}
// Create a function to clear the memory allocated for a linked-list.
// The function will take in a pointer named "list" that points to the start of the linked-list.
void clearList (struct node* list) {
	// While the pointer named "list" is not NULL...
	while (list != NULL) {
		// Save a pointer to the next node of the linked-list in a variable named "next".
		struct node* next = list->nextNode;
		// Free the memory that was allocated for the current node in the linked-list.
		free(list);
		// Make the pointer named "list" point to whatever is pointed to by the pointer named "next".
		list = next;
	}
}
