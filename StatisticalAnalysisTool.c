// Create a function to determine the determinent of a matrix.
double det(double *matrix, int rows, int columns) {
	// If the matrix is less than 2x2 or the dimensions are unequal, return NAN.
	if (rows!=columns || rows*columns < 4) {
		return NAN;
	}
	// Otherwise, compute the determinant of the matrix and return it...
	else if (rows == 2 && columns == 2) {
		return matrix[0]*matrix[3] - matrix[1]*matrix[2];
	} else if (rows == 3 && columns == 3) {
		return (matrix[0]*matrix[4]*matrix[8] + matrix[1]*matrix[5]*matrix[6] + matrix[2]*matrix[3]*matrix[7]) - (matrix[2]*matrix[4]*matrix[6] + matrix[0]*matrix[5]*matrix[7] + matrix[1]*matrix[3]*matrix[8]);
	} else {
		// Create a new double variable named "detn" and initialize its value to 0.
		double detn = 0;
		// Create a new integer variable named "sign1" and initialize its value to 1.
		int sign1 = 1;
		// Create a new integer variable named "sign2" and initialize its value to -1.
		int sign2 = -1;
		// Dynamically allocate memory for a new matrix named "newMatrix". This will contain a subset of the values from the original matrix.
		double *newMatrix = (double*)(malloc((rows-1)*(columns-1)*sizeof(double)));
		while (newMatrix == NULL) {
			newMatrix = (double*)(malloc((rows-1)*(columns-1)*sizeof(double)));
		}
		// Copy values from the original matrix to the new matrix.
		for (int a = 0; a < rows*columns; a++) {
			int count = 0;
			int row = a/columns;
			int column = a%columns;
			for (int b = 0; b < rows*columns; b++) {
				if (!((b/columns) == row || (b%columns == column))) {
					newMatrix[count] = matrix[b];
					count++;
				}
			}
			// Compute the determinent of the matrix current matrix using Laplace expansion. Store the result in the double variable named "detn".
			detn = detn + sign1*pow(sign2,a/columns)*matrix[a]*det(newMatrix,rows-1,columns-1);
			if (a%columns != 0) {
				sign1 = -sign1;
			} else {
				sign1 = 1;
			}
		}
		// Free the memory that was allocated for the matrix named "newMatrix".
		free(newMatrix);
		// Return the value of the variable named "detn";
		return detn;
	}
}
// Create a function to compute the dot product of two vectors.
double dotProduct (double vector1[], double vector2[], int size) {
	// Create a new double variable named "result" and initialize its value to zero.
	double result = 0;
	// Compute the dot product of the two vectors.
	for (int i = 0; i < size; i++) {
		result = result + vector1[i]*vector2[i];
	}
	// Return the dot product of the two vectors.
	return result;
}
